Usage: gql-federation-schema-parser [options] [command]

CLI tool to parse and manipulate GraphQL schemas for federation

Options:
  -V, --version    output the version number
  -D --debug       Enable debug mode (default: false)
  -S --simple      Disable colors on terminal (default: false)
  -h, --help       display help for command

Commands:
  parse [options]  Parse GraphQL schemas
  help [command]   display help for command